package to.avax.avalanche.network;


public class NetworkConfigRpc {
    private String c;
    private String x;
    private String p;

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }
}
